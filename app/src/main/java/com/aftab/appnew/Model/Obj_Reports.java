package com.aftab.appnew.Model;

public class Obj_Reports {

    private String Title;

    private String Size;
    private String Size_number;
    private String Size_number_m;
    private String Min;
    private String max;
    private String avg;



    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public String getSize() {
        return Size;
    }

    public void setSize(String size) {
        Size = size;
    }

    public String getSize_number() {
        return Size_number;
    }

    public void setSize_number(String size_number) {
        Size_number = size_number;
    }

    public String getSize_number_m() {
        return Size_number_m;
    }

    public void setSize_number_m(String size_number_m) {
        Size_number_m = size_number_m;
    }

    public String getMin() {
        return Min;
    }

    public void setMin(String min) {
        Min = min;
    }

    public String getMax() {
        return max;
    }

    public void setMax(String max) {
        this.max = max;
    }

    public String getAvg() {
        return avg;
    }

    public void setAvg(String avg) {
        this.avg = avg;
    }
}
